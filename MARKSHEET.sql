-- 1. Show all student names and their math marks sorted by highest math score.

SELECT NAME,MATH
FROM MARKSHEET
ORDER BY MATH DESC;



-- 2. Find all students who are from DHAKA and scored more than 80 in BIOLOGY.

SELECT NAME,ADDRESS,BIOLOGY
FROM MARKSHEET
WHERE ADDRESS = "DHAKA" AND BIOLOGY>80;



-- 3. Show the total marks (sum of math, physics, chemistry, biology) for each student.

SELECT NAME,(MATH+PHYSICS+CHEMISTRY+BIOLOGY) AS TOTAL_MARKS
FROM MARKSHEET;



-- 4. Find the student with the highest total marks.

SELECT NAME,(MATH+PHYSICS+CHEMISTRY+BIOLOGY) AS TOTAL_MARKS
FROM MARKSHEET
ORDER BY TOTAL_MARKS DESC
LIMIT 1;



-- 5. Count how many students are from each address (DHAKA, KHULNA, BARISHAL, RAJSHAHI).

SELECT ADDRESS, COUNT(*) AS HOMETOWN_COUNT
FROM MARKSHEET
GROUP BY ADDRESS;



-- 6. Find the average marks in physics for FEMALE students.

SELECT GENDER,AVG (PHYSICS) AS AVG_PHYSICS_MARK
FROM MARKSHEET
WHERE GENDER = "FEMALE";



-- 7. List all students whose total marks are below 250.

SELECT NAME, (MATH+PHYSICS+CHEMISTRY+BIOLOGY) AS TOTAL_MARKS
FROM MARKSHEET
WHERE (MATH+PHYSICS+CHEMISTRY+BIOLOGY) < 250;



-- 8. Display all students sorted by address and then by name alphabetically.

SELECT *
FROM MARKSHEET
ORDER BY ADDRESS ASC, NAME ASC;



-- 9. How many MALE and FEMALE students are there?

SELECT GENDER, COUNT(GENDER) AS GENDER_COUNT
FROM MARKSHEET
GROUP BY GENDER;



-- 10. Show students who have scored at least 90 in all four subjects (MATH, PHYSICS, CHEMISTRY, BIOLOGY).

SELECT NAME,MATH, PHYSICS, CHEMISTRY, BIOLOGY
FROM MARKSHEET
WHERE MATH >= 90 AND PHYSICS >= 90 AND CHEMISTRY >= 90 AND BIOLOGY >= 90;


